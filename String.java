

String str = "Hello World!";
System.out.println("String: " + str);
System.out.println("Length: " + str.length());
System.out.println("Substring: " + str.substring(0, 5));
System.out.println("Substring: " + str.substring(6));
System.out.println("Concat: " + str.concat(" This is a test."));
System.out.println("Upper: " + str.toUpperCase());
System.out.println("Lower: " + str.toLowerCase());
System.out.println("Trim: " + str.trim());
System.out.println("Replace: " + str.replace('l', 'w'));
System.out.println("Char at: " + str.charAt(6));
System.out.println("Index of: " + str.indexOf("World"));
System.out.println("Starts with: " + str.startsWith("Hello"));
System.out.println("Ends with: " + str.endsWith("World!"));
